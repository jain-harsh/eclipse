package dynamic_processing;

import java.util.Scanner;

public class buying_fruits {
public static void main(String[] args) {
	Scanner scn = new Scanner(System.in);
	int t=scn.nextInt();
	while(t>0) {
		int n=scn.nextInt();
		for(int i=0;i<n;i++) {
			int[] arr=new int[3];
			for(int j=0;j<arr.length;j++) {
				arr[j]=scn.nextInt();
			}
			
		}
		
		
		
		
		t--;
	}	
		
}	
}	
